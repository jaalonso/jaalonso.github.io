% * Hechos 1-4: 6 y 12 son divisibles por 2 y por 3.
% * Hecho 5: 4 es divisible por 2.
% * Regla 1: Los números divisibles por 2 y por 3 son divisibles por 6.

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% § Programa                                                                %%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

divide(2,6).                               % Hecho 1
divide(2,4).                               % Hecho 2
divide(2,12).                              % Hecho 3
divide(3,6).                               % Hecho 4
divide(3,12).                              % Hecho 5
divide(6,X) :- divide(2,X), divide(3,X).   % Regla 1

