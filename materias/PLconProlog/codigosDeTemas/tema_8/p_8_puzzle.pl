% Heurística 2
% ============

% Nota: Se usarán dos heurísticas definidas por heurística_1 y
% heurística_2

% con_heuristica(+NH) redefine heuristica(E,H) como heuristica_NH(E,H).
con_heuristica(NH) :-
   abolish(heuristica,2),
   ( NH=0 -> assert(heuristica(E,H) :- heuristica_0(E,H))
   ; NH=1 -> assert(heuristica(E,H) :- heuristica_1(E,H))
   ; NH=2 -> assert(heuristica(E,H) :- heuristica_2(E,H))).

% heuristica_0(+E,?H) se verifica si la heurística H del estado E es 0.
heuristica_0(_E,0).

% heuristica_1(+E,?H) se verifica si H es el número de piezas colocadas
% en E en distinto lugar que en el estado final.
heuristica_1(E,H) :-
   estado_final(EF),
   diferencias(E,EF,H).

% heuristica_2(+E,?H) se verifica si H es la suma de distancias de las
% fichas del estado E hasta su posición en el estado final.
heuristica_2(E,H) :-
   distancias(E,L),
   suma_lista(L,H).

% distancias(+E,?L) se verifica si L es la lista de las distancias de
% las fichas del estado E hasta su posición en el estado final.
distancias(E,L) :-
   estado_final(F),
   findall(D,
           (member(X,E),
            X \== h,
            coordenadas(X,E,CE),
            coordenadas(X,F,CF),
            distancia(CE,CF,D)),
           L).

% coordenadas(+P,+E,?C) se verifica si C son las coordenadas de la ficha
% P en el estado E (se supone que las fichas de la lista tienen como
% coordenadas [[0,0],[0,1],..,[2,2]]).
coordenadas(P,E,[X,Y]) :-
   nth0(N,E,P),
   X is N//3,
   Y is N mod 3.

% distancia(+P1,+P2,?D) se verifica si D es la distancia Manhatan entre
% los puntos P1 y P2.
distancia([X1,Y1],[X2,Y2],D) :-
   D is abs(X1-X2)+abs(Y1-Y2).

% suma_lista(+L,?N) se verifica si N es la suma de los números de la
% lista de números L.
suma_lista([X|R],N) :-
   suma_lista(R,N1),
   N is X+N1.
suma_lista([],0).

