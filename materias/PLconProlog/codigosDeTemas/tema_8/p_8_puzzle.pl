escribe_solucion(S) :-
   format('~nSol: ~n'),
   escribe_lista_de_estados(S).

% escribe_lista_de_estados(+L) escribe la lista de estados L. Por
% ejemplo, 
%    ?- escribe_lista_de_estados([[2,8,3,1,6,4,7,h,5],[2,8,3,1,h,4,7,6,5]]).
%    +---+  +---+  
%    |283|  |283|  
%    |164|  |1 4|  
%    |7 5|  |765|  
%    +---+  +---+  
%    true 
escribe_lista_de_estados(L) :-
   length(L,N),
   N < 11, !,
   escribe_tope(N),
   escribe_filas(1,L),
   escribe_filas(2,L),
   escribe_filas(3,L),
   escribe_tope(N).
escribe_lista_de_estados(L) :-
   % length(L,N),
   % N >= 11,
   append(L1,L2,L),
   length(L1,10),
   escribe_lista_de_estados(L1), nl,
   escribe_lista_de_estados(L2).

escribe_tope(N) :-
   N > 0,
   N1 is N-1,
   write('+'),
   write('-'), write('-'), write('-'),
   write('+'), write('  '),
   escribe_tope(N1).
escribe_tope(0) :-
   nl.

escribe_filas(I,[E|R]) :-
   escribe_fila(I,E),
   escribe_filas(I,R).
escribe_filas(_,[]) :-
   nl.

escribe_fila(1,[A,B,C|_]) :-
   write('|'),
   escribe(A), escribe(B), escribe(C),
   write('|'), write('  ').
escribe_fila(I,[_,_,_|R]) :-
   I > 1,
   I1 is I-1,
   escribe_fila(I1,R).

escribe(X) :-
   ( X=h       -> write(' ')
   ; otherwise -> write(X)).

