% Heurística 1
% ============

% heuristica(+E,?H) se verifica si H es el número de piezas colocadas en
% E en distinto lugar que en el estado final.
heuristica(E,H) :-
   estado_final(EF),
   diferencias(E,EF,H).

% diferencias(+L1,+L2,?D) se verifica si D es el número de elmentos de
% la lista L1 distintos de los correspondientes en L2.
diferencias([X|R1],[Y|R2],D) :-
   diferencias(R1,R2,D1),
   ( (X==h; X==Y) -> D is D1
   ; otherwise    -> D is D1+1).
diferencias([],[],0).

