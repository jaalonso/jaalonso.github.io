% oración(O) se verifica si O es una oración de la gramática
%    oración          --> sintagma_nominal, sintagma_verbal
%    sintagma_nominal --> nombre
%    sintagma_nominal --> artículo, nombre
%    sintagma_verbal  --> verbo, sintagma_nominal
%    artículo         --> [el]
%    nombre           --> [gato]
%    nombre           --> [perro]
%    nombre           --> [pescado]
%    nombre           --> [carne]
%    verbo            --> [come]
% Por ejemplo,
%    ?- oración([el,gato,come,pescado]).
%    true 
%    ?- oración([el,come,pescado]).
%    false.
%    ?- oración([el,perro,come,carne]).
%    true 
%    ?- oración([el,perro,come,pienso]).
%    false.
oración(O) :-
   sintagma_nominal(SN),
   sintagma_verbal(SV),
   append(SN,SV,O).

sintagma_nominal(SN) :-
   nombre(SN).
sintagma_nominal(SN) :-
   artículo(A),
   nombre(N),
   append(A,N,SN).

sintagma_verbal(SV) :-
   verbo(V),
   sintagma_nominal(SN),
   append(V,SN,SV).

artículo([el]).   
nombre([gato]).    
nombre([perro]).
nombre([pescado]). 
nombre([carne]).
verbo([come]).

